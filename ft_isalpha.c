#include <unistd.h>
#include <stdio.h>

int	ft_isalpha(char c)
{
	if ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z'))
		return (1);
	else
		return (0);
}

int	main(void)
{
	char	example;

	example = 'a';
	printf("%d", ft_isalpha(example));
}
