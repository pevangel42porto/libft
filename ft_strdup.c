#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*temp;
	int		i;

	temp = (char *) malloc(ft_strlen(s) + 1 * sizeof(char));
	i = 0;
	while (s[i] != '\0')
	{
		temp[i] = s[i];
		i++;
	}
	temp[i] = '\0';
	return (temp);
}
